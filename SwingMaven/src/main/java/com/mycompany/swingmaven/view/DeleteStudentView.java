     private void jButton1ActionPerformed(java.awt.event.ActionEvent evt){
        int ansFromUser = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xoá không" , "Xoá" , JOptionPane.OK_CANCEL_OPTION);  
        // Chon 'OK' tra ve gia tri 0 
        if(ansFromUser == 0){
            int selectedRow = content.getSelectedRow();
            Student student = students.get(selectedRow);
               
            var db = HibernateUtil.getEntityManager();

            db.getTransaction().begin();
            db.remove(db.find(Student.class, student.getId()));
            db.getTransaction().commit();
            viewData();
        }
     }
