    private void viewData(){
        var db = HibernateUtil.getEntityManager();
        TypedQuery<Student> query = db.createQuery("from Student", Student.class);
        students = query.getResultList();

        TableModel tableModel = content.getModel();
        for(int i = 0; i < students.size(); i++){
            Student student = students.get(i);
            
            tableModel.setValueAt(student.getId(), i, 0);
            tableModel.setValueAt(student.getName(), i, 1);
            tableModel.setValueAt(student.getClassName(), i, 2);
            tableModel.setValueAt(student.getEmail(), i, 3);
            tableModel.setValueAt(student.getPhoneNumber(), i, 4);
            tableModel.setValueAt(student.getBirthday(), i, 5);
        }
    }
